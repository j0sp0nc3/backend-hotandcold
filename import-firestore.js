// import-firestore.js
// Script para importar datos desde un archivo JSON de backup a Firestore

const { db } = require('./config/firebaseAdmin');
const fs = require('fs');
const path = require('path');

async function importarDatos(backupFile) {
  try {
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
    console.log('üî• IMPORTANDO DATOS A FIRESTORE');
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');

    // Determinar archivo de backup
    let filepath = backupFile;
    if (!filepath) {
      // Buscar el backup m√°s reciente
      const files = fs.readdirSync(__dirname)
        .filter(f => f.startsWith('backup_firebase_') && f.endsWith('.json'))
        .sort()
        .reverse();

      if (files.length === 0) {
        throw new Error('‚ùå No se encontr√≥ archivo de backup. Usa: node import-firestore.js <archivo.json>');
      }
      filepath = files[0];
    }

    // Leer archivo de backup
    const fullPath = path.join(__dirname, filepath);
    if (!fs.existsSync(fullPath)) {
      throw new Error(`‚ùå Archivo no encontrado: ${filepath}`);
    }

    console.log(`üìÇ Leyendo backup: ${filepath}\n`);
    const backup = JSON.parse(fs.readFileSync(fullPath, 'utf-8'));

    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    // IMPORTAR USUARIOS
    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    if (backup.usuarios && backup.usuarios.length > 0) {
      console.log(`üì§ Importando ${backup.usuarios.length} usuarios...`);
      let success = 0;
      let failed = 0;

      for (const user of backup.usuarios) {
        try {
          const { id, ...datos } = user;
          await db.collection('usuarios').doc(id).set(datos);
          success++;
        } catch (error) {
          console.error(`   ‚ö†Ô∏è  Error al importar usuario ${user.id}: ${error.message}`);
          failed++;
        }
      }
      console.log(`   ‚úÖ ${success} usuarios importados, ${failed} fallidos\n`);
    }

    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    // IMPORTAR MENSAJES
    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    if (backup.mensajes && backup.mensajes.length > 0) {
      console.log(`üì§ Importando ${backup.mensajes.length} mensajes...`);
      let success = 0;
      let failed = 0;

      for (const msg of backup.mensajes) {
        try {
          const { id, timestamp, ...datos } = msg;
          const docData = {
            ...datos,
            timestamp: timestamp ? new Date(timestamp) : new Date()
          };
          await db.collection('mensajes').doc(id).set(docData);
          success++;
        } catch (error) {
          console.error(`   ‚ö†Ô∏è  Error al importar mensaje ${msg.id}: ${error.message}`);
          failed++;
        }
      }
      console.log(`   ‚úÖ ${success} mensajes importados, ${failed} fallidos\n`);
    }

    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    // IMPORTAR CONTACTOS
    // ‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê
    if (backup.contactos && backup.contactos.length > 0) {
      console.log(`üì§ Importando ${backup.contactos.length} contactos...`);
      let success = 0;
      let failed = 0;

      for (const contacto of backup.contactos) {
        try {
          const { id, timestamp, ...datos } = contacto;
          const docData = {
            ...datos,
            timestamp: timestamp ? new Date(timestamp) : new Date()
          };
          await db.collection('contactos').doc(id).set(docData);
          success++;
        } catch (error) {
          console.error(`   ‚ö†Ô∏è  Error al importar contacto ${contacto.id}: ${error.message}`);
          failed++;
        }
      }
      console.log(`   ‚úÖ ${success} contactos importados, ${failed} fallidos\n`);
    }

    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
    console.log('‚úÖ IMPORT COMPLETADO');
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
    console.log(`üìä Resumen final:`);
    console.log(`   - Usuarios: ${backup.resumen.total_usuarios}`);
    console.log(`   - Mensajes: ${backup.resumen.total_mensajes}`);
    console.log(`   - Contactos: ${backup.resumen.total_contactos}`);
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');

    console.log('üí° Pr√≥ximo paso: Verifica los datos en Firebase Console');
    console.log('   https://console.firebase.google.com\n');
  } catch (error) {
    console.error('\n‚ùå ERROR EN IMPORTACI√ìN:');
    console.error(error.message);
    console.error(error.stack);
    process.exit(1);
  }
}

// Obtener archivo de backup de argumentos
const backupFile = process.argv[2];

if (require.main === module) {
  importarDatos(backupFile).catch(error => {
    console.error('Error fatal:', error);
    process.exit(1);
  });
}

module.exports = { importarDatos };
